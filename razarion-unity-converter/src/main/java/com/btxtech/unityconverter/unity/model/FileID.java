package com.btxtech.unityconverter.unity.model;

public class FileID {
    private String fileID;

    public String getFileID() {
        return fileID;
    }

    public void setFileID(String fileID) {
        this.fileID = fileID;
    }

    @Override
    public String toString() {
        return "FileID{" +
                "fileID='" + fileID + '\'' +
                '}';
    }
}
